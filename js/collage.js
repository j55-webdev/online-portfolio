/* First Block */
$('#firstBlock').mouseover(function(){
	$('#firstBlock').css('background', 'white');
	$('#firstText').css('display', 'unset');
	return;
});
$('#firstBlock').mouseout(function(){
	$('#firstBlock').css('background', 'url("https://i.imgur.com/Rui0hj8.jpg")');
	$('#firstBlock').css('background-repeat', 'no-repeat');
	$('#firstBlock').css('background-size', '100%');
	$('#firstText').css('display', 'none');
	return;
});

